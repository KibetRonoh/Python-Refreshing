friends = ['kibet', 'Paul', 'Pato', 'Richie', 'anna', 'Rael']

######
# the first number is the starting point and the second is the ending point
######
print(friends[2:4]) # gets friends 2 and 3, excludes 4
print(friends[1:]) # get all the elements except the first one
print(friends[:4]) # start from the beginning and exclud 4
print(friends[:]) # gives you a new list
print(friends[-3:]) # starts counting from the left -1, -2,-3
print(friends[:-2]) # count from the beginning upto the 2nd last element
print(friends[-3:-1])
